    public class LocalBinder extends Binder {
        BluetoothLeService getService() {
            return BluetoothLeService.this;
        }
    }

    protected void onReceiveCommand(byte[] packet)
    {
        if(isNotiShowing.getAndSet(true))
            return;

        Intent intent = new Intent(this, MainActivity.class);
        PendingIntent pendingIntent = PendingIntent.getActivity(this, 0, intent,PendingIntent.FLAG_UPDATE_CURRENT);

        String title;
        String content;
        String ticker;

        switch (packet[0])
        {
            case TYPE_TEMP:
                title = "온도 경고";
                if(packet[1] < LEVEL_WARN)
                    return;
                content = "실내 온도가 너무 높습니다!!";
                break;
            case TYPE_SOUND:
                title = "초인종 알림";
                if(packet[1] < LEVEL_WARN)
                    return;
                content = "누군가 초인종을 누른 것 같습니다!!";
                break;
            case TYPE_WATER:
                title = "물넘침 경고";
                if(packet[1] < LEVEL_WARN)
                    return;
                content = "혹시 물을 켜두시진 않으셨나요?";
                break;
            default:
                return;
        }

        if(packet[1] < LEVEL_WARN || packet[0] == TYPE_SOUND)
            ticker = "알림!!";
        else
            ticker = "경고!!";

        mNotification = new Notification.Builder(getApplicationContext())
                .setContentTitle(title)
                .setContentText(content)
                .setSmallIcon(R.drawable.ic_launcher)
                .setTicker(ticker)
                .setContentIntent(pendingIntent)
                .build();

        //소리추가
        mNotification.defaults = Notification.DEFAULT_SOUND;

        //알림 소리를 한번만 내도록
        mNotification.flags = Notification.FLAG_ONLY_ALERT_ONCE;

        //확인하면 자동으로 알림이 제거 되도록
        mNotification.flags = Notification.FLAG_AUTO_CANCEL;
        mNotification.flags = Notification.PRIORITY_MAX;

        mNotificationManager.notify(777, mNotification);
        PushWakeLock.acquireCpuWakeLock(this);
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                try{
                    PushWakeLock.releaseCpuLock();
                    Thread.sleep(5000,0);
                }catch (Exception ex)
                {
                    ex.printStackTrace();
                }
                isNotiShowing.set(false);
            }
        });
        thread.start();
    }

